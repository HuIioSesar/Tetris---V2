    #Draw tiles by name or by matrix
    def draw_tile(self, tile_or_name, x_offset, y_offset, screen):
        """Draw a tile by name or by matrix."""
        # If it's a string, get the tile from dictionary
        if isinstance(tile_or_name, str):
            tile = self.tiles.get(tile_or_name)
            if tile is None:
                return
        else:
            # Assume it's already a matrix
            tile = tile_or_name

        for row in range(self.tile_size):
            for col in range(self.tile_size):
                value = tile[row][col]
                color = self.colours.get(value, (0, 0, 0))
                pygame.draw.rect(screen, color, (
                    x_offset + col * self.scale,
                    y_offset + row * self.scale,
                    self.scale,
                    self.scale
                ))


